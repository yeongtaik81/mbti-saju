export function getCompatibilityScenarioContentProfile(
  scenarioCode: ScenarioCode | null | undefined,
  subjectType: CompatibilityRelationType
): CompatibilityScenarioContentProfile {
  const base = DEFAULT_COMPATIBILITY_PROFILE[subjectType];

  if (scenarioCode && scenarioCode in COMPATIBILITY_SCENARIO_PROFILES) {
    return {
      ...base,
      ...(COMPATIBILITY_SCENARIO_PROFILES[
        scenarioCode
      ] as Partial<CompatibilityScenarioContentProfile>)
    };
  }

  return base;
}
